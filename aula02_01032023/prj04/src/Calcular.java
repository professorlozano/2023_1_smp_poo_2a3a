public class Calcular {
    public void Somar(int a, int b){
        System.out.println(a+b);
    }
    
    public void Somar (int a, int b, int c){
        System.out.println(a+b+c);
    }
    
    public void Somar (int a, int b, int c, int d){
        System.out.println(a+b+c+d);
    }
}
